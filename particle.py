import json


class Particle:
	"""
	Particle.

	TODO: Longer class definition.

	Attributes:
		atomic_number: An integer representing the number of protons in this
			particle.
		name: A string of the name of this element.
		symbol: A two character string of this element's symbol.
		mass: A float indicating the molar mass of this particle.
		valence: An int indicating the valence number of this particle.
		electronegativity: A float indicating the electronegativity of this
			particle.
		charges: A dict mapping a string charge name to a float charge value.
	"""

	def __init__(self, atomic_number):
		element = element_database.get(atomic_number)
		assert element is not None, 'Atomic number {} undiscovered!'.format(atomic_number)
		self.atomic_number = atomic_number
		self.name = element['name']
		self.symbol = element['symbol']
		self.mass = element['mass']
		self.valence = element['valence']
		self.electronegativity = element['electronegativity']
		self.charges = element['charges']


	def __repr__(self):
		return 'Name: {}\n\tAtomic number: {}\n\tSymbol: {}\n\tMass: {}\n\tValence: {}\n\tEN: {}'.format(
			self.name,
			self.atomic_number,
			self.symbol,
			self.mass,
			self.valence,
			self.electronegativity)

		



